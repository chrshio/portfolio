import { execSync } from "child_process";
import { writeFileSync, mkdirSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const outPath = join(root, "lib", "last-updated.ts");

let dateStr = null;
try {
  const iso = execSync("git log -1 --format=%ad --date=short", {
    encoding: "utf8",
    cwd: root,
  }).trim();
  if (iso) {
    const [y, m, d] = iso.split("-").map(Number);
    const date = new Date(y, m - 1, d);
    dateStr = date.toLocaleDateString("en-US", { month: "short", day: "numeric" });
  }
} catch {
  // no git or not a repo
}

const content = `// Generated by scripts/generate-last-updated.mjs (run at build time)\nexport const lastUpdated = ${dateStr ? JSON.stringify(dateStr) : "null"};\n`;
mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, content);
